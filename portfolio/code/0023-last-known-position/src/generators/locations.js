/**
 * Generate a single address object with all required properties.
 * Returns { street, city, state, zip, full } — always.
 */
function generateAddress(rng) {
  const county = pick(rng, COUNTIES);
  const zip = pick(rng, county.zips);

  // House number
  const houseNumber = randInt(rng, 1, 12000);

  // Street name — ordinal or named
  const useOrdinal = chance(rng, 0.25);
  const streetName = useOrdinal ? pick(rng, STREET_NAMES_ORDINAL) : pick(rng, STREET_NAMES);
  const streetType = pick(rng, STREET_TYPES);

  // Optional directional prefix
  const useDir = chance(rng, 0.15);
  const directionals = ["N", "S", "E", "W", "NE", "NW", "SE", "SW"];
  const dir = useDir ? pick(rng, directionals) + " " : "";

  const street = `${houseNumber} ${dir}${streetName} ${streetType}`;
  const city = `${county.name} County`;
  const state = county.state;

  return {
    street,
    city,
    state,
    zip,
    full: `${street}, ${city}, ${state} ${zip}`
  };
}

/**
 * Get a random county object.
 */
function getRandomCounty(rng) {
  return pick(rng, COUNTIES);
}

/**
 * Get a random ZIP from a county.
 */
function getRandomZip(county, rng) {
  return pick(rng, county.zips);
}

/**
 * Generate a plausible apartment/unit number.
 */
function generateUnit(rng) {
  if (!chance(rng, 0.3)) return null;
  const unitTypes = ["Apt", "Unit", "Ste"];
  const unitType = pick(rng, unitTypes);
  if (chance(rng, 0.5)) {
    return `${unitType} ${randInt(rng, 1, 400)}`;
  }
  const floor = randInt(rng, 1, 15);
  const letter = String.fromCharCode(65 + randInt(rng, 0, 5));
  return `${unitType} ${floor}${letter}`;
}

